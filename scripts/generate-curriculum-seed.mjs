import { courses } from './curriculum-data.mjs'
import { writeFileSync } from 'node:fs'

function sqlStr(v) {
  if (v === null || v === undefined) return 'NULL'
  return `'${String(v).replace(/'/g, "''")}'`
}

function newId() {
  const bytes = crypto.getRandomValues(new Uint8Array(16))
  return Array.from(bytes).map((b) => b.toString(16).padStart(2, '0')).join('')
}

const LESSON_MINUTES = 15
const TOKEN_COST = 1

const lines = []
lines.push('-- Generated by scripts/generate-curriculum-seed.mjs — do not hand-edit. Re-run the generator instead.')

for (const course of courses) {
  const durationMinutes = course.lessons.length * LESSON_MINUTES
  lines.push(
    `INSERT OR IGNORE INTO courses (id, category_id, title, slug, description, level, duration_minutes, token_cost, published) VALUES (${sqlStr(course.id)}, ${sqlStr(course.categoryId)}, ${sqlStr(course.title)}, ${sqlStr(course.slug)}, ${sqlStr(course.description)}, ${sqlStr(course.level)}, ${durationMinutes}, ${TOKEN_COST}, 1);`,
  )
  course.lessons.forEach((lesson, i) => {
    const id = `${course.id}-l${i + 1}`
    lines.push(
      `INSERT OR IGNORE INTO lessons (id, course_id, title, description, content, position, duration_minutes) VALUES (${sqlStr(id)}, ${sqlStr(course.id)}, ${sqlStr(lesson.title)}, ${sqlStr(lesson.description)}, ${sqlStr(lesson.content)}, ${i + 1}, ${LESSON_MINUTES});`,
    )
  })
}

writeFileSync('scripts/curriculum-seed.sql', lines.join('\n') + '\n')
console.log(`Wrote scripts/curriculum-seed.sql — ${courses.length} courses, ${courses.reduce((n, c) => n + c.lessons.length, 0)} lessons.`)
